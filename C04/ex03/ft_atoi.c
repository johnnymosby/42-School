int	ft_atoi(char *str)
{
	int	i;
	int	n;
	int	number;

	i = 0;
	n = 1;
	number = 0;
	while (str[i] == ' ' || str[i] == '\f' || str[i] == '\n'
		|| str[i] == '\r' || str[i] == '\t' || str[i] == '\v')
	{
		i++;
	}
	while (str[i] == '+' || str[i] == '-')
	{
		if (str[i] == '-')
			n *= -1;
		i++;
	}
	while ('0' <= str[i] && str[i] <= '9')
	{
		number = number * 10 + (str[i] - '0');
		i++;
	}
	return (n * number);
}
